local symbols = {}
for key, val in pairs(symbols_map) do
    local lhs = "<" .. vim.g.symbol_insert_modifier .. "-" .. key .. ">"

    if type(val) == "string" then
        table.insert(symbols, {lhs, val})
    else
        for suffix, _val in pairs(val) do
            table.insert(symbols, {lhs .. suffix, _val})
        end
    end
end

return symbols
