local symbols_map = {
    -----------------------------[ lowercase letters ]------------------------------
    ["e"] = "∈",
    ["g"] = greek.lowercase,

    -----------------------------[ uppercase letters ]------------------------------
    ["A"] = "Ɐ",
    ["C"] = "ℂ",
    ["E"] = "Ǝ",
    ["G"] = greek.uppercase,
    ["S"] = "∫",

    ----------------------------------[ numbers ]-----------------------------------
    ["8"] = "∞",
    ["0"] = "°",

    ----------------------------------[ symbols ]-----------------------------------
    ['-'] = "—",
    ["="] = "≠",
    ["<"] = "≤",
    [">"] = "≥",
    ["."] = "∅",
    ['^'] = superscripts,
    ['&'] = subscripts,

    -----------------------------------[ arrows ]-----------------------------------
    ["Left"] = "←",
    ["Right"] = "→",
    ["Up"] = "↑",
    ["Down"] = "↓",
}

local symbols = {}
for key, val in pairs(symbols_map) do
    local lhs = "<" .. modifier .. "-" .. key .. ">"

    if type(val) == "string" then
        table.insert(symbols, {lhs, val})
    else
        for suffix, _val in pairs(val) do
            table.insert(symbols, {lhs .. suffix, _val})
        end
    end
end

return symbols
